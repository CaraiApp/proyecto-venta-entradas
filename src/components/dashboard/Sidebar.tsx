export function Sidebar() {
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);
  const { user } = useAuthContext();
  const pathname = usePathname();

  // Determinar el menú según el rol del usuario
  let navigation: SidebarItem[] = [];

  // Menú para clientes
  const customerNavigation: SidebarItem[] = [
    { name: "Dashboard", href: "/dashboard", icon: HomeIcon },
    {
      name: "Mis Entradas",
      href: "/dashboard/customer/tickets",
      icon: TicketIcon,
    },
    {
      name: "Historial de Compras",
      href: "/dashboard/customer/orders",
      icon: ReportIcon,
    },
    { name: "Mi Perfil", href: "/dashboard/customer/profile", icon: UserIcon },
  ];

  // Menú para organizadores
  const organizerNavigation: SidebarItem[] = [
    { name: "Dashboard", href: "/dashboard", icon: HomeIcon },
    {
      name: "Mis Eventos",
      href: "/dashboard/organizer/events",
      icon: EventIcon,
    },
    {
      name: "Crear Evento",
      href: "/dashboard/organizer/events/create",
      icon: EventIcon,
    },
    { name: "Miembros", href: "/dashboard/organizer/members", icon: UserIcon },
    {
      name: "Mapas de Asientos",
      href: "/dashboard/organizer/seating-maps",
      icon: MapIcon,
    },
    {
      name: "Informes",
      href: "/dashboard/organizer/reports",
      icon: ReportIcon,
    },
    {
      name: "Configuración",
      href: "/dashboard/organizer/settings",
      icon: SettingsIcon,
    },
  ];

  // Menú para administradores
  const adminNavigation: SidebarItem[] = [
    { name: "Dashboard", href: "/dashboard", icon: HomeIcon },
    {
      name: "Organizaciones",
      href: "/dashboard/admin/organizations",
      icon: OrganizationIcon,
    },
    { name: "Usuarios", href: "/dashboard/admin/users", icon: UserIcon },
    { name: "Eventos", href: "/dashboard/admin/events", icon: EventIcon },
    {
      name: "Mapas de Asientos",
      href: "/dashboard/admin/seating-maps",
      icon: MapIcon,
    },
    { name: "Informes", href: "/dashboard/admin/reports", icon: ReportIcon },
    {
      name: "Configuración",
      href: "/dashboard/admin/settings",
      icon: SettingsIcon,
    },
  ];

  // Asignar el menú según el rol
  if (user?.user_metadata?.role === "admin") {
    navigation = adminNavigation;
  } else if (user?.user_metadata?.role === "organizer") {
    navigation = organizerNavigation;
  } else {
    navigation = customerNavigation;
  }

  const isActive = (href) => {
    return pathname === href || pathname.startsWith(href + "/");
  };

  return (
    <div className="h-full flex">
      {/* Barra lateral para escritorio */}
      <div className="hidden md:flex md:flex-shrink-0">
        <div className="flex flex-col w-64">
          <div className="flex flex-col h-0 flex-1 bg-gray-800">
            <div className="flex-1 flex flex-col pt-5 pb-4 overflow-y-auto">
              <div className="flex items-center flex-shrink-0 px-4">
                <h2 className="text-white text-xl font-bold">Dashboard</h2>
              </div>
              <nav className="mt-5 flex-1 px-2 space-y-1">
                {navigation.map((item) => (
                  <Link
                    key={item.name}
                    href={item.href}
                    className={`group flex items-center px-2 py-2 text-sm font-medium rounded-md ${
                      isActive(item.href)
                        ? "bg-gray-900 text-white"
                        : "text-gray-300 hover:bg-gray-700 hover:text-white"
                    }`}
                  >
                    <item.icon
                      className={`mr-3 flex-shrink-0 h-6 w-6 ${
                        isActive(item.href)
                          ? "text-gray-300"
                          : "text-gray-400 group-hover:text-gray-300"
                      }`}
                      aria-hidden="true"
                    />
                    {item.name}
                  </Link>
                ))}
              </nav>
            </div>
          </div>
        </div>
      </div>

      {/* Barra lateral móvil */}
      <div className="md:hidden">
        <div className="fixed inset-0 flex z-40">
          <button
            className="fixed inset-0 bg-gray-600 bg-opacity-75"
            aria-hidden="true"
            onClick={() => setIsMobileMenuOpen(false)}
          ></button>
          <div
            className={`fixed inset-y-0 left-0 flex-1 flex flex-col max-w-xs w-full bg-gray-800 transform transition ease-in-out duration-300 ${
              isMobileMenuOpen ? "translate-x-0" : "-translate-x-full"
            }`}
          >
            <div className="absolute top-0 right-0 -mr-12 pt-2">
              <button
                className="ml-1 flex items-center justify-center h-10 w-10 rounded-full focus:outline-none focus:ring-2 focus:ring-inset focus:ring-white"
                onClick={() => setIsMobileMenuOpen(false)}
              >
                <span className="sr-only">Cerrar barra lateral</span>
                <svg
                  className="h-6 w-6 text-white"
                  xmlns="http://www.w3.org/2000/svg"
                  fill="none"
                  viewBox="0 0 24 24"
                  stroke="currentColor"
                  aria-hidden="true"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth="2"
                    d="M6 18L18 6M6 6l12 12"
                  />
                </svg>
              </button>
            </div>
            <div className="flex-1 h-0 pt-5 pb-4 overflow-y-auto">
              <div className="flex-shrink-0 flex items-center px-4">
                <h2 className="text-white text-xl font-bold">Dashboard</h2>
              </div>
              <nav className="mt-5 px-2 space-y-1">
                {navigation.map((item) => (
                  <Link
                    key={item.name}
                    href={item.href}
                    className={`group flex items-center px-2 py-2 text-base font-medium rounded-md ${
                      isActive(item.href)
                        ? "bg-gray-900 text-white"
                        : "text-gray-300 hover:bg-gray-700 hover:text-white"
                    }`}
                    onClick={() => setIsMobileMenuOpen(false)}
                  >
                    <item.icon
                      className={`mr-4 flex-shrink-0 h-6 w-6 ${
                        isActive(item.href)
                          ? "text-gray-300"
                          : "text-gray-400 group-hover:text-gray-300"
                      }`}
                      aria-hidden="true"
                    />
                    {item.name}
                  </Link>
                ))}
              </nav>
            </div>
          </div>
        </div>
      </div>

      {/* Botón para abrir menú móvil */}
      <div className="md:hidden fixed top-16 left-4 z-10">
        <button
          className="p-2 rounded-md text-gray-500 hover:text-white hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-inset focus:ring-white"
          onClick={() => setIsMobileMenuOpen(true)}
        >
          <span className="sr-only">Abrir menú</span>
          <svg
            className="h-6 w-6"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            stroke="currentColor"
            aria-hidden="true"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="M4 6h16M4 12h16M4 18h16"
            />
          </svg>
        </button>
      </div>
    </div>
  );
}
